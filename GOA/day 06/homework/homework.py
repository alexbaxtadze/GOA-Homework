
#case გამოიყენება პითონშიც და html და ასე შემდეგ. ის ორგვარია case-sensitive და case-insensitive



#case-sensitive არის პითონში ხოლო case-insensitive არის html
#case-sensitive ეს ნიშნავს, იმას რომ დიდი ასოებით დაწერ თუ პატარათი ამას დიდი მნიშვნელობა აქვს, პატარა ასოთი დაწერილს 
#სხვა მნიშვნელობას მივიხებთ აქ დიდი აოსთი კი სხვა
# 
#  
#ხოლო case-insensitive ნიშნავს დიდ ასოებს დაწერ თუ პატარას არააქვს მნიშვნელობა


